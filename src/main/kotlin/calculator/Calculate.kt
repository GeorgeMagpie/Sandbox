package calculator

class Calculate {

    fun sum(x: Int, y: Int) = x + y
    fun sum(x: Double, y: Double) = x + y

    fun subtract(x: Int, y: Int) = x - y
    fun subtract(x: Double, y: Double) = x - y

    fun division(x: Int, y: Int) = x / y
    fun division(x: Double, y: Double) = x / y

    fun multiplication(x: Int, y: Int) = x * y
    fun multiplication(x: Double, y: Double) = x * y


}